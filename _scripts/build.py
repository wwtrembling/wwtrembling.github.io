PREBUILT_LANGS_AUTHORED = ['en', 'ko', 'ja', 'de', 'hi', 'id', 'pt', 'th', 'vi']
PREBUILT_LANGS_CLONED = ['zh-cn', 'zh-tw']

# Block of <link rel="alternate" ...> entries for hand-authored prebuilt tool
# pages. We replace whatever the page currently has between the "<!-- Language
# Alternatives -->" marker and the next blank line, so old 9-language blocks
# get refreshed to include zh-cn / zh-tw.
import re as _re


def _build_prebuilt_alternates(tool_path: str) -> str:
    lines = ['  <!-- Language Alternatives -->']
    for l in LANGUAGES:
        hl = hreflang_for(l)
        lines.append(f'  <link rel="alternate" hreflang="{hl}" href="https://utilifyapp.net/{l}/{tool_path}/">')
    lines.append(f'  <link rel="alternate" hreflang="x-default" href="https://utilifyapp.net/en/{tool_path}/">')
    return '\n'.join(lines)


def _refresh_alternates(html: str, tool_path: str) -> str:
    block = _build_prebuilt_alternates(tool_path)
    pattern = _re.compile(
        r'[ \t]*<!--\s*Language Alternatives\s*-->\s*\n'
        r'(?:[ \t]*<link\s+rel="alternate"[^>]*>\s*\n)+',
        _re.IGNORECASE,
    )
    if pattern.search(html):
        return pattern.sub(block + '\n', html, count=1)
    # No existing block — insert before </head>.
    return html.replace('</head>', block + '\n</head>', 1)


def _retarget_lang(html: str, tool_path: str, dst_lang: str) -> str:
    """Convert an English-locale HTML page into another locale by rewriting
    the `lang` attribute, JSON-LD `inLanguage`, and the canonical/og:url for
    /en/<tool>/. Only the *self* URL is rewritten — alternate links are
    regenerated separately via _refresh_alternates."""
    out = html.replace('<html lang="en">', f'<html lang="{dst_lang}">', 1)
    out = out.replace('"inLanguage": "en"', f'"inLanguage": "{dst_lang}"')
    out = out.replace(
        f'href="https://utilifyapp.net/en/{tool_path}/"',
        f'href="https://utilifyapp.net/{dst_lang}/{tool_path}/"',
    )
    out = out.replace(
        f'content="https://utilifyapp.net/en/{tool_path}/"',
        f'content="https://utilifyapp.net/{dst_lang}/{tool_path}/"',
    )
    out = out.replace(
        f'"url": "https://utilifyapp.net/en/{tool_path}/"',
        f'"url": "https://utilifyapp.net/{dst_lang}/{tool_path}/"',
    )
    # Header logo link.
    out = out.replace(f'href="/en/"', f'href="/{dst_lang}/"')
    return out


def sync_prebuilt_tools():
    print("Syncing prebuilt tool pages (hreflang refresh + zh-cn/zh-tw clones)...")
    for tool_path, _icon, _tool_dict in PREBUILT_TOOLS:
        # Refresh authored language pages so they include zh-cn / zh-tw alternates.
        for lang in PREBUILT_LANGS_AUTHORED:
            page = os.path.join(BASE_DIR, lang, tool_path, 'index.html')
            if not os.path.exists(page):
                continue
            with open(page, 'r', encoding='utf-8') as f:
                content = f.read()
            new_content = _refresh_alternates(content, tool_path)
            new_content = inject_monetization(new_content)
            new_content = inject_legal_links_resolved(new_content, lang)
            if new_content != content:
                with open(page, 'w', encoding='utf-8') as f:
                    f.write(new_content)
                print(f"  refreshed {lang}/{tool_path}/index.html")

        # Clone the English page into zh-cn / zh-tw so those locales link to a
        # page rather than 404. The clone is a one-shot fallback — we only
        # write it if no page exists, so a future hand-translated version is
        # not overwritten.
        en_page = os.path.join(BASE_DIR, 'en', tool_path, 'index.html')
        if not os.path.exists(en_page):
            print(f"  WARN: missing en/{tool_path}/index.html, skipping clones")
            continue
        with open(en_page, 'r', encoding='utf-8') as f:
            en_content = f.read()
        for clone_lang in PREBUILT_LANGS_CLONED:
            clone_dir = os.path.join(BASE_DIR, clone_lang, tool_path)
            clone_path = os.path.join(clone_dir, 'index.html')
            if os.path.exists(clone_path):
                # Existing clone: still refresh hreflang + monetization so prior
                # builds don't keep stale AdSense scripts.
                with open(clone_path, 'r', encoding='utf-8') as f:
                    existing = f.read()
                refreshed = _refresh_alternates(existing, tool_path)
                refreshed = inject_monetization(refreshed)
                refreshed = inject_legal_links_resolved(refreshed, clone_lang)
                if refreshed != existing:
                    with open(clone_path, 'w', encoding='utf-8') as f:
                        f.write(refreshed)
                    print(f"  refreshed clone {clone_lang}/{tool_path}/index.html")
                continue
            retargeted = _retarget_lang(en_content, tool_path, clone_lang)
            retargeted = _refresh_alternates(retargeted, tool_path)
            retargeted = inject_monetization(retargeted)
            retargeted = inject_legal_links_resolved(retargeted, clone_lang)
            os.makedirs(clone_dir, exist_ok=True)
            with open(clone_path, 'w', encoding='utf-8') as f:
                f.write(retargeted)
            print(f"  cloned en/{tool_path}/ → {clone_lang}/{tool_path}/")


