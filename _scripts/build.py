def load_template(template_name):
    path = os.path.join(TEMPLATE_DIR, template_name)
    try:
        with open(path, 'r', encoding='utf-8') as f:
            content = f.read()
        return inject_monetization(content)
    except FileNotFoundError:
        print(f"  WARN: template not found, skipping: {template_name}")
        return None


# --- Monetization injection ----------------------------------------------------
# Site is monetized via Google AdSense Auto Ads. We inject a single AdSense
# loader script in <head> and a single Auto Ads enable trigger before </body>.
# Auto Ads chooses placement automatically — no per-slot divs or slot IDs.
# Funding Choices CMP for EU/UK consent is added by the user separately.
ADSENSE_CLIENT = "ca-pub-6334819180242631"

import re as _re_ad


def inject_monetization(html):
    """Idempotently strip Ezoic remnants (CMP/sa.min.js/analytics/ezstandalone
    bootstrap, ad placeholder divs, showAds trigger) and inject the AdSense
    loader + Auto Ads trigger. Also injects legal footer links.
    """
    # 1. Strip Ezoic CMP scripts (both gatekeeperconsent endpoints).
    html = _re_ad.sub(
        r'\s*<script[^>]*\bsrc="https://(?:cmp|the)\.gatekeeperconsent\.com/[^"]*"[^>]*></script>\s*',
        '\n    ', html,
    )
    # 2. Strip Ezoic SA loader.
    html = _re_ad.sub(
        r'\s*<script[^>]*\bsrc="https://www\.ezojs\.com/ezoic/sa\.min\.js"[^>]*></script>\s*',
        '\n    ', html,
    )
    # 3. Strip Ezoic analytics loader.
    html = _re_ad.sub(
        r'\s*<script[^>]*\bsrc="https://ezoicanalytics\.com/[^"]*"[^>]*></script>\s*',
        '\n    ', html,
    )
    # 4. Strip the inline ezstandalone bootstrap block.
    html = _re_ad.sub(
        r'\s*<script>\s*window\.ezstandalone\s*=[\s\S]*?ezstandalone\.cmd\s*=[\s\S]*?</script>\s*',
        '\n    ', html,
    )
    # 5. Strip the "<!-- Ezoic -->" comment header.
    html = _re_ad.sub(r'\s*<!--\s*Ezoic\s*-->\s*', '\n    ', html)
    # 6. Strip any leftover ezoic-pub-ad-placeholder divs.
    html = _re_ad.sub(
        r'\s*<div\s+id="ezoic-pub-ad-placeholder-\d+"></div>\s*',
        '\n    ', html,
    )
    # 7. Strip the ezstandalone.showAds() trigger.
    html = _re_ad.sub(
        r'\s*<script>[^<]*ezstandalone\.showAds\([^)]*\);[^<]*</script>\s*',
        '\n', html,
    )

    # 7b. Strip legacy commented-out AdSense loader blocks left over from
    #     prior cleanup ("<!-- Google AdSense Removed for Developer Experience -->"
    #     followed by a commented-out script tag). Otherwise their substring
    #     match would defeat the idempotency check below and we'd skip
    #     injecting the live loader.
    html = _re_ad.sub(
        r'\s*<!--\s*Google AdSense[^-]*-->\s*\n?'
        r'(?:\s*<!--\s*Google AdSense Removed[^-]*-->\s*\n?)?'
        r'(?:\s*<!--\s*<script\s+async\s+src="https://pagead2\.googlesyndication\.com[^>]*>\s*</script>\s*-->\s*\n?)?',
        '\n    ', html,
    )

    # 8. Inject the AdSense loader once in <head>. Use a *unique* marker
    #    string in the inserted comment so the idempotency check is robust
    #    against random substrings in the page.
    if 'data-utilify-adsense-loader' not in html:
        loader = (
            '    <script data-utilify-adsense-loader async '
            f'src="https://pagead2.googlesyndication.com/pagead/js/adsbygoogle.js?client={ADSENSE_CLIENT}" '
            'crossorigin="anonymous"></script>\n'
        )
        html = html.replace('</head>', loader + '</head>', 1)

    # 9. Inject the Auto Ads enable trigger once before </body>.
    if 'enable_page_level_ads' not in html:
        trigger = (
            '    <script>(adsbygoogle=window.adsbygoogle||[]).push({'
            f'google_ad_client:"{ADSENSE_CLIENT}",enable_page_level_ads:true'
            '});</script>\n'
        )
        html = html.replace('</body>', trigger + '</body>', 1)

    # 9b. Inject OG image + Twitter card meta once in <head>. Marker
    #     `data-utilify-og` guards idempotency against re-runs.
    if 'data-utilify-og' not in html:
        og_block = (
            '    <meta data-utilify-og property="og:image" content="https://utilifyapp.net/assets/og-image.png">\n'
            '    <meta property="og:image:width" content="1200">\n'
            '    <meta property="og:image:height" content="630">\n'
            '    <meta property="og:site_name" content="Utilify">\n'
            '    <meta name="twitter:card" content="summary_large_image">\n'
            '    <meta name="twitter:image" content="https://utilifyapp.net/assets/og-image.png">\n'
        )
        html = html.replace('</head>', og_block + '</head>', 1)

    # 9d. Inject favicon links once in <head>. Marker `data-utilify-favicon`
    #     guards idempotency. Browsers auto-request /favicon.ico anyway, but
    #     advertising the multi-size set yields a sharper tab/bookmark icon
    #     and gives Apple/Android proper home-screen icons.
    if 'data-utilify-favicon' not in html:
        favicon_block = (
            '    <link data-utilify-favicon rel="icon" type="image/x-icon" href="/favicon.ico">\n'
            '    <link rel="icon" type="image/png" sizes="16x16" href="/favicon-16x16.png">\n'
            '    <link rel="icon" type="image/png" sizes="32x32" href="/favicon-32x32.png">\n'
            '    <link rel="apple-touch-icon" sizes="180x180" href="/apple-touch-icon.png">\n'
        )
        html = html.replace('</head>', favicon_block + '</head>', 1)

    # 9f. Dark-mode init: read localStorage 'utilify_theme' (or
    #     prefers-color-scheme as fallback) and set `data-theme` on <html>
    #     before paint. Inlined and synchronous — runs as the first thing
    #     in <head> after meta charset, so the body never renders in the
    #     wrong theme.
    if 'data-utilify-theme-init' not in html:
        theme_init = (
            '    <script data-utilify-theme-init>(function(){try{'
            'var s=localStorage.getItem("utilify_theme");'
            'var t=(s==="dark"||s==="light")?s:'
            '(window.matchMedia&&window.matchMedia("(prefers-color-scheme: dark)").matches?"dark":"light");'
            'document.documentElement.setAttribute("data-theme",t);'
            '}catch(e){}})();</script>\n'
        )
        html = html.replace('</head>', theme_init + '</head>', 1)

    # 9g. Inject the theme-toggle button next to the site logo + the click
    #     handler before </body>. The button uses {{ theme_toggle_aria }}
    #     for its aria-label; the placeholder resolves at template-fill time
    #     for `{{ lang_code }}` templates and is replaced inline for prebuilt
    #     pages with their resolved language string.
    if 'data-utilify-theme-toggle' not in html:
        is_template = '{{ lang_code }}' in html
        if is_template:
            aria_label = '{{ theme_toggle_aria }}'
        else:
            # Detect language from <html lang="..."> for prebuilt pages.
            lang_match = _re_ad.search(r'<html[^>]+lang="([^"]+)"', html)
            lang_code = lang_match.group(1) if lang_match else 'en'
            common_en = COMMON.get('en', {})
            common_lang = COMMON.get(lang_code, {})
            aria_label = (common_lang.get('theme_toggle_aria')
                          or common_en.get('theme_toggle_aria')
                          or 'Toggle dark mode')
        toggle_btn = (
            f'<button data-utilify-theme-toggle id="themeToggle" '
            f'class="theme-toggle" aria-label="{aria_label}" type="button">🌙</button>'
        )
        # Insert immediately after the logo's closing </a> tag.
        html = _re_ad.sub(
            r'(<a\s+href="[^"]+"\s+class="site-logo"[^>]*>[^<]*</a>)',
            r'\1' + toggle_btn,
            html, count=1,
        )
        # Append handler before </body>.
        toggle_handler = (
            '    <script data-utilify-theme-toggle-script>(function(){'
            'var b=document.getElementById("themeToggle");if(!b)return;'
            'function p(){var t=document.documentElement.getAttribute("data-theme")||"light";'
            'b.textContent=t==="dark"?"☀️":"🌙";'
            'b.setAttribute("aria-pressed",String(t==="dark"));}'
            'p();b.addEventListener("click",function(){'
            'var c=document.documentElement.getAttribute("data-theme")||"light";'
            'var n=c==="dark"?"light":"dark";'
            'document.documentElement.setAttribute("data-theme",n);'
            'try{localStorage.setItem("utilify_theme",n);}catch(e){}p();});'
            '})();</script>\n'
        )
        html = html.replace('</body>', toggle_handler + '</body>', 1)

    # 9e. Inject favorites star toggle into tool-page security-badge row, plus
    #     the init script before </body>. Only on templates (`{{ lang_code }}`
    #     placeholder present) AND tool pages (`container-narrow` layout) — the
    #     index uses a wider `.container` and would render the button next to
    #     the install button awkwardly.
    if ('{{ lang_code }}' in html and 'container-narrow' in html
            and 'data-utilify-fav-toggle' not in html):
        fav_btn_html = (
            '\n                    <button data-utilify-fav-toggle '
            'class="fav-toggle-page" type="button" '
            'aria-label="{{ fav_add_aria }}" '
            'data-add-label="{{ fav_add_aria }}" '
            'data-remove-label="{{ fav_remove_aria }}">☆</button>'
        )
        # Insert after the install button (matches both block and inline placeholder formats).
        html = _re_ad.sub(
            r'(<button id="pwaInstallBtn"[^>]*>\s*\{\{\s*install_app\s*\}\}\s*</button>)',
            lambda m: m.group(1) + fav_btn_html,
            html, count=1,
        )
        fav_init = (
            '    <script data-utilify-fav-toggle-script>(function(){'
            'var btn=document.querySelector(".fav-toggle-page");'
            'if(!btn)return;'
            'var m=location.pathname.match(/^\\/[^\\/]+\\/([^\\/]+)\\/?$/);'
            'if(!m)return;'
            'var slug=m[1],K="utilify_favorites";'
            'function g(){return (localStorage.getItem(K)||"").split(",").filter(Boolean);}'
            'function s(a){localStorage.setItem(K,Array.from(new Set(a)).join(","));}'
            'function p(f){btn.textContent=f?"★":"☆";'
            'btn.classList.toggle("is-fav",f);'
            'btn.setAttribute("aria-label",f?btn.dataset.removeLabel:btn.dataset.addLabel);'
            'btn.setAttribute("aria-pressed",String(f));}'
            'p(g().indexOf(slug)>-1);'
            'btn.addEventListener("click",function(){var a=g(),'
            'n=a.indexOf(slug)>-1?a.filter(function(x){return x!==slug;}):a.concat([slug]);'
            's(n);p(n.indexOf(slug)>-1);});})();</script>\n'
        )
        html = html.replace('</body>', fav_init + '</body>', 1)

    # 9c. Inject skip-to-content link as first child of <body>, and ensure
    #     <main> has id="main" so the skip link has a target. Idempotent
    #     via marker `data-utilify-skip`.
    if 'data-utilify-skip' not in html:
        if '{{ lang_code }}' in html:
            link_text = '{{ skip_link }}'
        else:
            link_text = 'Skip to content'
        skip = (
            f'\n    <a data-utilify-skip class="skip-link" href="#main">{link_text}</a>'
        )
        html = _re_ad.sub(r'(<body[^>]*>)', r'\1' + skip, html, count=1)
        # Ensure <main ...> has id="main"; only patch if missing.
        if '<main' in html and 'id="main"' not in html:
            html = _re_ad.sub(
                r'(<main\b)([^>]*)(>)',
                lambda m: m.group(1) + ' id="main"' + m.group(2) + m.group(3),
                html, count=1,
            )

    # 10. Inject legal-page links into footer of *templates* (those still have
    #     the {{ lang_code }} placeholder). For prebuilt hand-authored pages we
    #     inject lang-resolved links separately in sync_prebuilt_tools().
    if '{{ lang_code }}' in html and 'data-legal-links' not in html:
        legal_block = (
            '\n                <p data-legal-links style="font-size: 0.875rem; margin-top: 8px;">'
            '<a href="/{{ lang_code }}/about/">{{ link_about }}</a> · '
            '<a href="/{{ lang_code }}/privacy/">{{ link_privacy }}</a> · '
            '<a href="/{{ lang_code }}/terms/">{{ link_terms }}</a>'
            '</p>'
        )
        html = _re_ad.sub(
            r'(<p>\{\{ footer \}\}</p>)',
            r'\1' + legal_block,
            html, count=1,
        )

    return html


def inject_legal_links_resolved(html, lang):
    """Insert legal-page footer links with the language code already
    substituted. Used for prebuilt pages that arrive at sync time without
    {{ lang_code }} placeholders. Idempotent."""
    if 'data-legal-links' in html:
        return html
    common = COMMON.get('en', {}).copy()
    common.update(COMMON.get(lang, {}))
    about = common.get('link_about', 'About')
    privacy = common.get('link_privacy', 'Privacy')
    terms = common.get('link_terms', 'Terms')
    block = (
        f'\n                <p data-legal-links style="font-size: 0.875rem; margin-top: 8px;">'
        f'<a href="/{lang}/about/">{about}</a> · '
        f'<a href="/{lang}/privacy/">{privacy}</a> · '
        f'<a href="/{lang}/terms/">{terms}</a>'
        f'</p>'
    )
    # Insert before </footer>'s inner closing div.
    out = _re_ad.sub(
        r'(</div>\s*</div>\s*</footer>)',
        block + r'\n            \1',
        html, count=1,
    )
    return out
